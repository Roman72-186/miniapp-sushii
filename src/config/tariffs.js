export function pricingToTable(pricing) {
  const table = {};
  for (const [key, value] of Object.entries(pricing || {})) {
    table[key] = value.months || { 1: value.price };
  }
  return table;
}

export function getTariffMonthPrice(pricing, tariffId, months = 1) {
  return pricing?.[tariffId]?.months?.[months]
    || pricing?.[tariffId]?.[months]
    || pricing?.[tariffId]?.price
    || DEFAULT_PRICE_TABLE[tariffId]?.[months]
    || 0;
}
